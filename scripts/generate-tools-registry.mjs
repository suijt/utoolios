#!/usr/bin/env node
/**
 * Registry generator (docs/06, docs/13). Scans packages/tools/<category>/<slug>/index.ts
 * and writes packages/tools/src/registry.generated.ts. This is what makes the M1 promise
 * real: add ONE folder, run this, and the tool is registered — no platform file is hand-edited.
 */
import { readdirSync, statSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const toolsRoot = join(here, '..', 'packages', 'tools')
const SKIP = new Set(['src', 'node_modules', 'dist'])

const entries = []
for (const category of readdirSync(toolsRoot)) {
  const catPath = join(toolsRoot, category)
  if (SKIP.has(category) || !statSync(catPath).isDirectory()) continue
  for (const slug of readdirSync(catPath)) {
    const toolPath = join(catPath, slug)
    if (!statSync(toolPath).isDirectory()) continue
    if (!existsSync(join(toolPath, 'index.ts'))) continue
    entries.push({ category, slug })
  }
}

entries.sort((a, b) => (a.category + a.slug).localeCompare(b.category + b.slug))

const toCamel = (s) => s.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase())
const importLines = entries
  .map((e) => `import ${toCamel(e.slug)} from '../${e.category}/${e.slug}/index'`)
  .join('\n')
const listLines = entries
  .map((e) => `  ${toCamel(e.slug)} as unknown as AnyToolPlugin,`)
  .join('\n')

const output = `// AUTO-GENERATED by scripts/generate-tools-registry.mjs — DO NOT EDIT.
// Regenerate after adding/removing a tool folder: pnpm --filter @utoolios/tools gen
import type { AnyToolPlugin } from '@utoolios/core'
${importLines}

export const tools: readonly AnyToolPlugin[] = [
${listLines}
]
`

writeFileSync(join(toolsRoot, 'src', 'registry.generated.ts'), output)
console.log(
  `[registry] generated ${entries.length} tool(s): ${entries.map((e) => e.slug).join(', ') || '(none)'}`,
)
